grade=input("Enter your grade : ")
if grade == "R":
    print("Your colour is red")
elif grade == "G":
    print("Your colour is green")
elif grade == "B":
    print("Your colour is blue")
elif grade == "v":
    print("Your colour is violet")
else:
    print("wrong code")